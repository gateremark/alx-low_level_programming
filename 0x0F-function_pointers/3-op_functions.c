#include <stdio.h>
#include "3-calc.h"

/**
 * op_add  - add nbrs
 * @a:a
 * @b:b
 * Return: 0.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract nbrs
 * @a:a
 * @b:b
 * Return: 0.
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplication
 * @a:a
 * @b:b
 * Return: 0.
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - division
 * @a: a
 * @b: b
 * Return: 0.
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - modulo
 * @a: a
 * @b: b
 * Return: 0.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
